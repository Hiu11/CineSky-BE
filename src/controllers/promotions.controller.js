const serializePromotion = (promotion) => ({
  id: String(promotion._id),
  kind: promotion.kind,
  tier: promotion.tier || "",
  tag: promotion.tag,
  title: promotion.title,
  value: promotion.value || "",
  code: promotion.code || "",
  discountType: promotion.discountType || "fixed",
  discountValue: Number(promotion.discountValue || 0),
  minOrderValue: Number(promotion.minOrderValue || 0),
  requiredPoints: Number(promotion.requiredPoints || 0),
  eligibleTiers: promotion.eligibleTiers || [],
  applicableGenres: promotion.applicableGenres || [],
  applicableComboIds: promotion.applicableComboIds || [],
  applicableWeekdays: promotion.applicableWeekdays || [],
  maxUsesPerUser: Number(promotion.maxUsesPerUser || 1),
  totalUsageLimit: Number(promotion.totalUsageLimit || 0),
  usedCount: Number(promotion.usedCount || 0),
  memberOnly: Boolean(promotion.memberOnly),
  theme: promotion.theme || "slate",
  startsAt: promotion.startsAt || null,
  endsAt: promotion.endsAt || null,
  description: promotion.description,
  order: Number(promotion.order || 0),
  isActive: Boolean(promotion.isActive),
  seedVersion: Number(promotion.seedVersion || 0),
  createdAt: promotion.createdAt,
  updatedAt: promotion.updatedAt,
});

const ensureDefaultPromotions = async () => {
  const operations = defaultPromotions.map((promotion) => {
    const filter = promotion.code ? { code: promotion.code } : { title: promotion.title };

    return {
      updateOne: {
        filter,
        update: [
          {
            $replaceRoot: {
              newRoot: {
                $cond: [
                  { $eq: ["$seedVersion", DEFAULT_PROMOTION_SEED_VERSION] },
                  "$$ROOT",
                  { $mergeObjects: ["$$ROOT", promotion] },
                ],
              },
            },
          },
        ],
        upsert: true,
      },
    };
  });

  if (operations.length) {
    await PromotionModel.bulkWrite(operations);
  }
};

const promotionsController = {
  getPromotions: async (req, res) => {
    try {
      await ensureDefaultPromotions();

      const promotions = await PromotionModel.find({ isActive: true }).sort({ order: 1, createdAt: 1 });
      const serializedPromotions = promotions.map(serializePromotion);

      return res.status(200).send({
        success: true,
        message: "Lấy danh sách ưu đãi thành công",
        data: {
          heroVoucher: serializedPromotions.find((item) => item.kind === "hero") || null,
          vouchers: serializedPromotions.filter((item) => item.kind === "member"),
          combos: serializedPromotions.filter((item) => item.kind === "combo"),
          all: serializedPromotions,
        },
      });
    } catch (error) {
      return res.status(500).send({
        success: false,
        message: error.message || "Lỗi máy chủ",
        data: null,
      });
    }
  },
};

export { defaultPromotions, ensureDefaultPromotions, serializePromotion };
export default promotionsController;




